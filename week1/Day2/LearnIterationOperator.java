package week1.Day2;

public class LearnIterationOperator {

	public static void main(String[] args) {
		int i = 1;
		int j = 1;
		
		System.out.println(i++);
		// uses the variable 1st i.e) post-incremental
		System.out.println(i);
		//update the value of the variable
		System.out.println(++j);
		//pre-incremental
	}

}
