package week1.Day2;

public class LearnIterationStatement {

	public static void main(String[] args) {
		int j=100;
		
		for (int i = 1; i <= 10; i++)
			
			//stage 1: declaring and initializing
			//stage 2: condition expression -> true / false
			//stage 3: Iteration -> ++ (increment) or -- (decrement)
			
		{
			System.out.println("The value is : " +i);
		}
		System.out.println(j);
	}

}
