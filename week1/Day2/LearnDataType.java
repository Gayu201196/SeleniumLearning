package week1.Day2;

public class LearnDataType {

	public static void main(String[] args) {
		// declaring a variable
		// type // identifier // '=' to assign my value to the variable
		
		byte variableOne = 10;
		short variableTwo = 30000;
		int variableThree = 100000;
		long phoneNum = 9988776655l;
		float scientificExp = 679.2456987f;
		double scientificExp2 = 647.47474752669745;
		char letter = 'J';
		boolean condition = true;
		
		System.out.println("My Data Type is byte "+variableOne);
		System.out.println("My Data Type is short "+variableTwo);
		System.out.println("My Data Type is int "+variableThree);
		System.out.println("My Data Type is long "+phoneNum);
		System.out.println("My Data Type is float "+scientificExp);
		System.out.println("My Data Type is double "+scientificExp2);
		System.out.println("My Data Type is char "+letter);
		System.out.println("My Data Type is boolean "+condition);
	}

}
